function toNote(txt) {
    const e = txt.split(' ');
    if (e[0] === '===') {
        return [Number(e[1]), 0];
    }
    const note = ['C-', 'C#', 'D-', 'D#', 'E-', 'F-', 'F#', 'G-', 'G#', 'A-', 'A#', 'B-', 'c-'].indexOf(e[0].substring(0, 2));
    let octave = e[0][2];
    if (note == 12) {
        octave--;
    }
    const noteval = (octave << 4) + note;
    return [Number(e[1]), noteval];
}
